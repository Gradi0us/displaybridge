"""Kotlin code generation for the DisplayBridge protocol (schema.yaml -> Messages.kt)."""
from __future__ import annotations

KT_PRIMITIVE_TYPE = {
    "u8": "Int",
    "u16": "Int",
    "u32": "Long",
    "u64": "Long",
    "varint": "Long",
    "string": "String",
    "bytes": "ByteArray",
}

KT_READ_METHOD = {
    "u8": "io.readU8()",
    "u16": "io.readU16()",
    "u32": "io.readU32()",
    "u64": "io.readU64()",
    "varint": "io.readVarint()",
    "string": "io.readString()",
    "bytes": "io.readBytes()",
}


def pascal(name: str) -> str:
    return name[0].upper() + name[1:]


def kt_field_type(field: dict) -> str:
    t = field["type"]
    if t == "list":
        item = field["item_type"]
        if item == "keyvalue":
            return "List<ConfigEntry>"
        if item == "touch_event":
            return "List<TouchEventItem>"
        return f"List<{KT_PRIMITIVE_TYPE[item]}>"
    return KT_PRIMITIVE_TYPE[t]


def kt_write_stmt(field: dict, accessor: str) -> str:
    t = field["type"]
    if t == "list":
        item = field["item_type"]
        if item == "keyvalue":
            return f"io.writeKeyValueList({accessor})"
        if item == "touch_event":
            return f"io.writeTouchEventList({accessor})"
        writer = {"u8": "io.writeU8(it)", "u16": "io.writeU16(it)", "u32": "io.writeU32(it)", "u64": "io.writeU64(it)"}[item]
        return f"io.writeList({accessor}) {{ {writer} }}"
    if t == "varint":
        return f"io.writeVarint({accessor})"
    if t == "string":
        return f"io.writeString({accessor})"
    if t == "bytes":
        return f"io.writeBytes({accessor})"
    method = {"u8": "writeU8", "u16": "writeU16", "u32": "writeU32", "u64": "writeU64"}[t]
    return f"io.{method}({accessor})"


def kt_read_expr(field: dict) -> str:
    t = field["type"]
    if t == "list":
        item = field["item_type"]
        if item == "keyvalue":
            return "io.readKeyValueList()"
        if item == "touch_event":
            return "io.readTouchEventList()"
        reader = {"u8": "io.readU8()", "u16": "io.readU16()", "u32": "io.readU32()", "u64": "io.readU64()"}[item]
        return f"io.readList {{ {reader} }}"
    return KT_READ_METHOD[t]


def generate_message_class(msg: dict) -> str:
    class_name = f"{msg['name']}Message"
    fields = msg["fields"]
    ctor_params = ",\n    ".join(f"val {f['name']}: {kt_field_type(f)}" for f in fields)
    lines = []
    lines.append(f"data class {class_name}(")
    lines.append(f"    {ctor_params}")
    lines.append(f") : ProtocolMessage {{")
    lines.append(f"    override val type: MessageType get() = MessageType.{msg['name']}")
    lines.append("")
    lines.append("    override fun writePayload(io: WireWriter) {")
    for f in fields:
        lines.append(f"        {kt_write_stmt(f, f['name'])}")
    lines.append("    }")
    lines.append("")
    lines.append("    companion object {")
    lines.append(f"        fun readPayload(io: WireReader): {class_name} {{")
    for f in fields:
        lines.append(f"            val {f['name']} = {kt_read_expr(f)}")
    args = ", ".join(f"{f['name']} = {f['name']}" for f in fields)
    lines.append(f"            return {class_name}({args})")
    lines.append("        }")
    lines.append("")
    lines.append(f"        fun deserialize(bytes: ByteArray): {class_name} = readPayload(WireReader(bytes))")
    lines.append("    }")
    lines.append("")
    lines.append("    fun serialize(): ByteArray = WireWriter().also { writePayload(it) }.toByteArray()")
    lines.append("}")
    return "\n".join(lines)


